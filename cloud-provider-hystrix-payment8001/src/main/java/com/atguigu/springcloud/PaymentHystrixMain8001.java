/**
 * @author lijian
 * @create 2021-01-31 14:07
 */
@SpringBootApplication
@EnableEurekaClient
@EnableCircuitBreaker       //启用降级断路器
public class PaymentHystrixMain8001 {
    public static void main(String[] args) {
        SpringApplication.run(PaymentHystrixMain8001.class,args);
    }
}
